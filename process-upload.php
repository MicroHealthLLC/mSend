// 5 minutes execution time
@set_time_limit(5 * 60);

// Uncomment this one to fake upload time
// usleep(5000);

// Settings
$targetDir = UPLOADED_FILES_FOLDER; 
$cleanupTargetDir = true; // Remove old files
$maxFileAge = 5 * 3600; // Temp file age in seconds


// Create target dir
if (!file_exists($targetDir)) {
	@mkdir($targetDir);
}

// Get a file name
if (isset($_REQUEST["name"])) {
	$fileName = $_REQUEST["name"];
} elseif (!empty($_FILES)) {
	$fileName = $_FILES["file"]["name"];
} else {
	$fileName = uniqid("file_");
}
$this_file = new PSend_Upload_File();
// Rename the file
$fileName = $this_file->safe_rename($fileName);

// Validate file has an acceptable extension
$fileExt = pathinfo($fileName, PATHINFO_EXTENSION);
$allowedExt = explode(',', ALLOWED_FILE_TYPES );
if ( false === CAN_UPLOAD_ANY_FILE_TYPE ) {
    if (!in_array($fileExt, $allowedExt)) {
        die('{"jsonrpc" : "2.0", "error" : {"code": 104, "message": "Invalid Extension."}, "id" : "id"}');
    };
}

// Make sure the fileName is unique but only if chunking is disabled
if ($chunks < 2) {
	$ext = strrpos($fileName, '.');
	$fileName_a = substr($fileName, 0, $ext);
	$fileName_b = substr($fileName, $ext);	$curr_usr_id= CURRENT_USER_ID;

	$count = 1;
	while (file_exists($targetDir . DIRECTORY_SEPARATOR .$fileName_a . '_' . $curr_usr_id. '_'.$count.$fileName_b))
	$count++;

	$fileName = $fileName_a . '_' . $curr_usr_id. '_' . $count.$fileName_b;
}

$filePath = $targetDir . DIRECTORY_SEPARATOR . $fileName;

// Chunking might be enabled
$chunk = isset($_REQUEST["chunk"]) ? intval($_REQUEST["chunk"]) : 0;
$chunks = isset($_REQUEST["chunks"]) ? intval($_REQUEST["chunks"]) : 0;


// Remove old temp files
if ($cleanupTargetDir) {
	if (!is_dir($targetDir) || !$dir = opendir($targetDir)) {
		die('{"jsonrpc" : "2.0", "error" : {"code": 100, "message": "Failed to open temp directory."}, "id" : "id"}');
	}

	while (($file = readdir($dir)) !== false) {
		$tmpfilePath = $targetDir . DIRECTORY_SEPARATOR . $file;

		// If temp file is current file proceed to the next
		if ($tmpfilePath == "{$filePath}.part") {
			continue;
		}

		// Remove temp file if it is older than the max age and is not the current file
		if (preg_match('/\.part$/', $file) && (filemtime($tmpfilePath) < time() - $maxFileAge)) {
			@unlink($tmpfilePath);
		}
	}
	closedir($dir);
}


// Open temp file
if (!$out = @fopen("{$filePath}.part", $chunks ? "ab" : "wb")) {
	die('{"jsonrpc" : "2.0", "error" : {"code": 102, "message": "Failed to open output stream."}, "id" : "id"}');
}

if (!empty($_FILES)) {
	if ($_FILES["file"]["error"] || !is_uploaded_file($_FILES["file"]["tmp_name"])) {
		die('{"jsonrpc" : "2.0", "error" : {"code": 103, "message": "Failed to move uploaded file."}, "id" : "id"}');
	}

	// Read binary input stream and append it to temp file
	if (!$in = @fopen($_FILES["file"]["tmp_name"], "rb")) {
		die('{"jsonrpc" : "2.0", "error" : {"code": 101, "message": "Failed to open input stream."}, "id" : "id"}');
	}
} else {
	if (!$in = @fopen("php://input", "rb")) {
		die('{"jsonrpc" : "2.0", "error" : {"code": 101, "message": "Failed to open input stream."}, "id" : "id"}');
	}
}

while ($buff = fread($in, 4096)) {
	fwrite($out, $buff);
}

@fclose($out);
@fclose($in);

// Check if file has been uploaded
if (!$chunks || $chunk == $chunks - 1) {
	// Strip the temp .part suffix off
	rename("{$filePath}.part", $filePath);



  $ext = pathinfo($fileName, PATHINFO_EXTENSION);
  if($ext !='zip'){
    $aes = new AESENCRYPT ();
    $aes->encryptFile($fileName);
    }
    else {
      $path = $filePath;
      $zip = new ZipArchive;
      $unzipped = array();
      if ($zip->open($path) === true) {
          for($i = 0; $i < $zip->numFiles; $i++) {
              $filename = $zip->getNameIndex($i);
              $fileinfo = pathinfo($filename);
              copy("zip://".$path."#".$filename, UPLOADED_FILES_FOLDER.'temp/'.$fileinfo['basename']);
              $unzipped[]= $fileinfo['basename'];
          }
          $zip->close();
      }
      if(!empty($unzipped)){
        $zipnew = new ZipArchive();
        $zipFilePath = UPLOADED_FILES_FOLDER.'temp/'.$fileName;
        $r = $zipnew->open($zipFilePath,  ZipArchive::CREATE);
        // //Decrypting invidual zip entries
        foreach ($unzipped as $unzip) {
          $aes = new AESENCRYPT();
          $aes->encryptZipFile($unzip);
          $zipnew->addFile(UPLOADED_FILES_FOLDER.'temp/'.$unzip,$unzip);
        }
        $r=$zipnew->close();
        //Deleting all encrypted zip entries extracted to temp
        foreach ($unzipped as $unzip) {
          unlink(UPLOADED_FILES_FOLDER.'temp/'.$unzip);
        }
        unlink($filePath);
        copy(UPLOADED_FILES_FOLDER.'temp/'.$fileName,$filePath);
        unlink(UPLOADED_FILES_FOLDER.'temp/'.$fileName);
      }
    }
    die('{"jsonrpc" : "2.0", "result" : null, "id" : "id", "NewFileName" : "'.$fileName.'"}');
}
